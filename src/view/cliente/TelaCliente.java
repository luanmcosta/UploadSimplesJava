    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // Atualizar dados de envio (arquivo e tamanho)
        refCliente.setArquivo(arquivoSelecionado);
        refCliente.setBytes(new byte[(int) arquivoSelecionado.length()]);
        
        // Enviar arquivo
        refCliente.enviarArquivo();
        
        // Atualizar interface
        lblStatusEnvio.setText("Arquivo enviado");
        btnEnviar.setEnabled(false);
    }//GEN-LAST:event_btnEnviarActionPerformed

    private void btnSelecionarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarArquivoActionPerformed
        
        // Instanciar janela de escolher arquivo
        escolherArquivo = new JFileChooser();
        escolherArquivo.setCurrentDirectory(new File("./"));
        
        // Abrir janela de busca e salvar resultado da busca
        int resultado = escolherArquivo.showOpenDialog(this);
        
        // Verificar se algum arquivo foi selecionado
        if (resultado == escolherArquivo.APPROVE_OPTION) {
            
            // Atribuir arquivo selecionado
            arquivoSelecionado = escolherArquivo.getSelectedFile();
            
            // Atualizar interface
            lblCaminhoArquivo.setText("<html>"+ arquivoSelecionado.getAbsolutePath() + "</html>");
            btnEnviar.setEnabled(true);
        }else{
            btnEnviar.setEnabled(false);
        }
    }//GEN-LAST:event_btnSelecionarArquivoActionPerformed

